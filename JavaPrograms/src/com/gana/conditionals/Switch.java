package com.gana.conditionals;

public class Switch {

	public static void main(String[] args) {
		int i = 4;
		switch(i) {
		case 1 :
			System.out.println("print the first statement");
			break;
		case 2 :
			System.out.println("print the second statement");
			break;
		case 3 :
			System.out.println("print the third statement");
			break;
		default :
			System.out.println("inavalid value");
				
		
		}

	}

}
