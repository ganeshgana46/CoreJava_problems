package com.statements;

public class Eg5 {

	public static void main(String[] args) {
		
		//use : when we the number of conditions in advance we can use for loop
		for(int i=0;i<=5;i++) {
			System.out.println(i);
		}
		
		System.out.println("--------");
		
		for(int i=5;i>=0;i--) {
			System.out.println(i);
		}

	}

}
