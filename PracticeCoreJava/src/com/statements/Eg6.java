package com.statements;

public class Eg6 {

	public static void main(String[] args) {
		
		//while loop : If we don't know the number of iterations in advance
		
		int i = 0;
		while(i<=5) {
			System.out.println(i);
			i++;
		}
		
		int j = 5;
		while(j>=0) {
			System.out.println(i);
			i--;
		}

	}

}
